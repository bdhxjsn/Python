print("~~~~~~~~~~~~~~~AND~~~~~~~~~~~~~~~")
print(True and True)#1 * 1
print(True and False)#1 * 0
print(False and True)#0 * 1
print(False and False)#0 * 0
print("~~~~~~~~~~~~~~~OR~~~~~~~~~~~~~~~~")
print(True or True)#1 + 1
print(True or False)#1 + 0
print(False or True)#0 + 1
print(False or False)#0 + 0
print("~~~~~~~~~~~~~~~NOT~~~~~~~~~~~~~~~")
print(not True)#!1 = 0
print(not False)#!0 = 1